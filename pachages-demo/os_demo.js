const os = require('os');

// Platform 

console.log(os.platform())

// CPU

console.log(os.arch()) // Architecture 64 bit
console.log(os.cpus()) // How many Core

// Free Memory

console.log(os.freemem())

// Total Memory 

console.log(os.totalmem())

// Home Directory

console.log(os.homedir())

// Up Time

console.log(os.uptime())





